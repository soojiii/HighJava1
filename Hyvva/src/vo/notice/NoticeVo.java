package vo.notice;

public class NoticeVo {
	
	private int notice_no;			//공지사항 게시판번호
	private String admin_id;		//관리자아이디
	private String notice_title;	//공지사항제목
	private String notice_content;	//공지사항 내용
	private String notice_date;		//공지사항 날짜
	private int notice_hits;		//공지사항 조회수
	private String notice_del;		//공지사항 삭제

	public int getNotice_no() {
		return notice_no;
	}
	public void setNotice_no(int notice_no) {
		this.notice_no = notice_no;
	}
	public String getAdmin_id() {
		return admin_id;
	}
	public void setAdmin_id(String admin_id) {
		this.admin_id = admin_id;
	}
	public String getNotice_title() {
		return notice_title;
	}
	public void setNotice_title(String notice_title) {
		this.notice_title = notice_title;
	}
	public String getNotice_content() {
		return notice_content;
	}
	public void setNotice_content(String notice_content) {
		this.notice_content = notice_content;
	}
	public String getNotice_date() {
		return notice_date;
	}
	public void setNotice_date(String notice_date) {
		this.notice_date = notice_date;
	}
	public int getNotice_hits() {
		return notice_hits;
	}
	public void setNotice_hits(int notice_hits) {
		this.notice_hits = notice_hits;
	}
	public String getNotice_del() {
		return notice_del;
	}
	public void setNotice_del(String notice_del) {
		this.notice_del = notice_del;
	}
	
}
