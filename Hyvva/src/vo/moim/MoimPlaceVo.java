package vo.moim;

public class MoimPlaceVo {
	private int place_id;
	private int m_cal_no;
	private String place_name;
	private String place_addr;
	private String place_detail_addr;
	private String start_time;
	
	public int getPlace_id() {
		return place_id;
	}
	public void setPlace_id(int place_id) {
		this.place_id = place_id;
	}
	public int getM_cal_no() {
		return m_cal_no;
	}
	public void setM_cal_no(int m_cal_no) {
		this.m_cal_no = m_cal_no;
	}
	public String getPlace_name() {
		return place_name;
	}
	public void setPlace_name(String place_name) {
		this.place_name = place_name;
	}
	public String getPlace_addr() {
		return place_addr;
	}
	public void setPlace_addr(String place_addr) {
		this.place_addr = place_addr;
	}
	public String getPlace_detail_addr() {
		return place_detail_addr;
	}
	public void setPlace_detail_addr(String place_detail_addr) {
		this.place_detail_addr = place_detail_addr;
	}
	public String getStart_time() {
		return start_time;
	}
	public void setStart_time(String start_time) {
		this.start_time = start_time;
	}
	
	
}
